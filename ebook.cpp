#include "ebook.h"  

// Конструктор класу EBook
// Ініціалізує поле fileSize як 0.0 (розмір файлу) і format як порожній рядок
EBook::EBook() : fileSize(0.0), format("") {}

// Метод для встановлення розміру файлу електронної книги
// Приймає значення s (тип double) і присвоює його полю fileSize
int EBook::SetFileSize(double s) {
    fileSize = s;  // Присвоюємо значення розміру
    return 1;      // Повертаємо 1 — успішне виконання
}

// Метод для встановлення формату електронної книги
// Приймає рядок f і присвоює його полю format
int EBook::SetFormat(string f) {
    format = f;    // Присвоюємо значення формату
    return 1;      // Повертаємо 1 — успішне виконання
}

// Метод для виведення інформації про електронну книгу
// Спочатку викликає метод Describe() з класу Book (виводить інформацію про автора, сторінки і т.д.)
// Потім додає виведення розміру файлу і формату
int EBook::Describe() {
    Book::Describe();  // Викликає Describe з класу Book
    cout << "Розмір файлу: " << fileSize << " МБ";  // Виводить розмір файлу в МБ
    cout << "\nФормат: " << format << endl;         // Виводить формат книги
    return 1;  // Повертає 1 — успішне виконання
}
